     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validateData = $request->validate([
            'pay_question' => 'required',
            'medicalAid_No' => 'max:255',
            'medical_aid_plans_id' => 'max:255',
            'user_id' => 'required',
            'dependent_id' => 'string|max:225',
            'medical_aid_scheme_id' => 'max:255',
        ]);
        Payment::create($validateData);
        //dd($request);
        $request->session()->flash('success', 'Your payment information saved successfully');
        return redirect(route('index'));
    }

